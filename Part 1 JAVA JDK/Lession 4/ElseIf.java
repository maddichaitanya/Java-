public class ElseIf {

    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        if (a > b) {
            System.out.println("java");
        } else if (a <= b) {
            System.out.println("java else if");
        } else {
            System.out.println("no java");
        }

    }
}