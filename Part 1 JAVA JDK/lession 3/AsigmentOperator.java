public class AsigmentOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = a;
        a += 10;
        int c = 10;
        c -= 20;
        int d = 1;
        d *= 2;
        int e = 2;
        e /= 3;

        System.out.println("=  assign a value to b" + b);
        System.out.println("+=  assign and add a value to a: " + a);
        System.out.println("-=  assign and add a value to c: " + c);
        System.out.println("*=  assign and add a value to d: " + d);
        System.out.println("/=  assign and add a value to e: " + e);

    }

}
