class Variables {

    public static void main(String args[]){

        byte z=2;                //  range Size = -128 to 127 
        short y=23;             //   2 bytes
        int a=5;                //   4 bytes
        long d=01234l;          //   8 bytes
        float b=1.05f;          //   4 bytes
        double c=1.234455;      //   8 bytes (Defalut) 
        

        char e='e';            //  2 bytes 
        boolean f=true;        //  1 bytes  (0 = ture or 1= false)

        System.out.println(z + " " + y);  // Concatentation in java 


    }
}